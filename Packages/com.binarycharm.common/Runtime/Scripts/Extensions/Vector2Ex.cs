using UnityEngine;

namespace BinaryCharm.Common.Extensions {

    public static class Vector2Ex {

        public static Vector2 withX(this Vector2 v, float x) {
            v.Set(x, v.y);
            return v;
        }

        public static Vector2 withY(this Vector2 v, float y) {
            v.Set(v.x, y);
            return v;
        }

    }

}
