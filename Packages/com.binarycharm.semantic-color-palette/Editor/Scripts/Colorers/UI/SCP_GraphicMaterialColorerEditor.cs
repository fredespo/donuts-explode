using UnityEditor;

namespace BinaryCharm.SemanticColorPalette.Colorers.UI {

    [CustomEditor(typeof(SCP_GraphicMaterialColorer)), CanEditMultipleObjects]
    public class SCP_GraphicMaterialColorerEditor : SCP_AMaterialColorerEditor {

        public override void OnInspectorGUI() {
            base.OnInspectorGUI();
            serializedObject.ApplyModifiedProperties();

            instanceSetup();
            updateAndDisplayColorProperties();

            serializedObject.ApplyModifiedProperties();
        }

    }

}
